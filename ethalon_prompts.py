# ---------------------------------------------------------------------------
# ПУБЛИЧНЫЕ ФУНКЦИИ
# ---------------------------------------------------------------------------

def build_system_prompt(
    settings: Settings,
    state: UserState,
    *,
    client_name: str | None = None,
    client_goal: str | None = None,
    followup_count: int = 0,
    memberships_text: str | None = None,
    services_text: str | None = None,
    hooks_text: str | None = None,
) -> str:
    core = _CORE.format(gym_name=settings.gym_name)

    club = _CLUB_BLOCK.format(
        gym_name=settings.gym_name,
        gym_address=settings.gym_address or "уточни у менеджера",
        gym_schedule=settings.gym_schedule or "уточни у менеджера",
        memberships_text=memberships_text or _DEFAULT_MEMBERSHIPS,
        services_text=services_text or _DEFAULT_SERVICES,
        hooks_text=hooks_text or _DEFAULT_HOOKS,
    )

    client = _CLIENT_BLOCK.format(
        client_name=client_name or "пока не знаем",
        client_goal=client_goal or "пока не выяснена",
        state=state.value,
        followup_count=followup_count,
    )

    instruction = _STATE_INSTRUCTIONS.get(state, "")
    return f"{core}\n{club}\n{client}\n{instruction}"


def build_followup_prompt(
    settings: Settings,
    followup_type: str,
    *,
    client_name: str | None = None,
    client_goal: str | None = None,
    followup_count: int = 0,
    memberships_text: str | None = None,
    services_text: str | None = None,
    hooks_text: str | None = None,
) -> str:
    core = _CORE.format(gym_name=settings.gym_name)

    club = _CLUB_BLOCK.format(
        gym_name=settings.gym_name,
        gym_address=settings.gym_address or "уточни у менеджера",
        gym_schedule=settings.gym_schedule or "уточни у менеджера",
        memberships_text=memberships_text or _DEFAULT_MEMBERSHIPS,
        services_text=services_text or _DEFAULT_SERVICES,
        hooks_text=hooks_text or _DEFAULT_HOOKS,
    )

    client = _CLIENT_BLOCK.format(
        client_name=client_name or "пока не знаем",
        client_goal=client_goal or "пока не выяснена",
        state="FOLLOW_UP",
        followup_count=followup_count,
    )

    instruction = FOLLOWUP_INSTRUCTIONS.get(followup_type, FOLLOWUP_INSTRUCTIONS["2h"])
    return f"{core}\n{club}\n{client}\n{instruction}"
