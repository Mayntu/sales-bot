def pick_agent_name(user_id: uuid.UUID) -> str:
    """Deterministically assign an agent name from settings based on user_id.

    The same user always talks to the same 'agent', which feels natural in
    a follow-up sequence. Uses the first 8 bytes of the UUID as a stable hash.
    """
    names = get_settings().get_agent_names()
    if not names:
        return "Саша"
    index = int(user_id.int % len(names))
    return names[index]


